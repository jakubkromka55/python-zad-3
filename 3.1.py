import math

# Obliczanie pierwiastka kwadratowego z 7
square_root = math.sqrt(7)
print('Pierwiastek kwadratowy z 7 wynosi:', square_root)

# Obliczanie logarytmu naturalnego z 5
natural_log = math.log(5)
print('Logarytm naturalny z 5 wynosi:', natural_log)

# Obliczanie sinusa 90 stopni (konwersja na radiany)
sine_90_degrees = math.sin(math.radians(90))
print('Sinus 90 stopni wynosi:', sine_90_degrees)
